public class Star {
    private String name;
    private double luminosity;
    private double mass;
    private double volume;
    private double age;

    public Star(String name, double luminosity, double mass, double volume, double age)
    {
        this.name = name;
        this.luminosity = luminosity;
        this.mass = mass;
        this.volume = volume;
        this.age = age;
    }
}